# Faça um programa que leia um número inteiro qualquer
# e mostre a sua tabuada\.

m = int(input('Digite um número inteiro: '))
print(f"\nA tabuada de {m} é:\n{m} x 0: {m*0}\n{m} x 1: {m*1}\n{m} x 2: {m*2}\n{m} x 3: {m*3}\n{m} x 4: {m*4}\n{m} x 5: {m*5}\n{m} x 6: {m*6}\n{m} x 7: {m*7}\n{m} x 8: {m*8}\n{m} x 9: {m*9}\n{m} x 10: {m*10}")